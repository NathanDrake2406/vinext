export type PublishCloudflarePrerenderedAppAssetsResult =
  | {
      publishedFiles: number;
      publishedRoutes: number;
      skipped: false;
    }
  | {
      publishedFiles: 0;
      publishedRoutes: 0;
      reason: string;
      skipped: true;
    };

const GENERATED_HEADERS_START = "# Static prerendered App Router assets (generated by vinext)";
const GENERATED_HEADERS_END = "# End static prerendered App Router assets";

const HTML_AUTHORITATIVE_HEADERS = [
  "Content-Type",
  "Cache-Control",
  VINEXT_CACHE_HEADER,
  NEXTJS_CACHE_HEADER,
];

const RSC_AUTHORITATIVE_HEADERS = [
  "Content-Type",
  "Cache-Control",
  "Vary",
  VINEXT_CACHE_HEADER,
  NEXTJS_CACHE_HEADER,
  VINEXT_RSC_COMPATIBILITY_ID_HEADER,
  NEXTJS_DEPLOYMENT_ID_HEADER,
];

// Cloudflare Static Assets caps `_headers` at 100 rules; a bundle that exceeds
// it is rejected at deploy time. Each published HTML route needs its own rule
// (visible paths share no prefix), so the count is real for large static apps.
// See https://developers.cloudflare.com/workers/static-assets/headers/
const CLOUDFLARE_HEADERS_RULE_LIMIT = 100;

function skipResult(reason: string): PublishCloudflarePrerenderedAppAssetsResult {
  return { skipped: true, reason, publishedFiles: 0, publishedRoutes: 0 };
}

/**
 * Count path rules in a `_headers` file. A rule is a line that begins a new
 * path pattern: non-blank, not indented (header lines are indented), and not a
 * comment. Used to budget generated rules against the Cloudflare limit.
 */
function countHeaderRules(content: string): number {
  let count = 0;
  for (const line of content.split("\n")) {
    if (line.length === 0 || /^\s/.test(line) || line.trimStart().startsWith("#")) continue;
    count++;
  }
  return count;
}

function hasMiddlewareOrProxy(root: string, config: ResolvedNextConfig): boolean {
  const matcher = createValidFileMatcher(config.pageExtensions);
  for (const conventionDir of [root, path.join(root, "src")]) {
    for (const ext of matcher.dottedExtensions) {
      if (
        fs.existsSync(path.join(conventionDir, `proxy${ext}`)) ||
        fs.existsSync(path.join(conventionDir, `middleware${ext}`))
      ) {
        return true;
      }
    }
  }
  return false;
}

function hasRequestTransformConfig(config: ResolvedNextConfig): boolean {
  return (
    config.headers.length > 0 ||
    config.redirects.length > 0 ||
    config.rewrites.beforeFiles.length > 0 ||
    config.rewrites.afterFiles.length > 0 ||
    config.rewrites.fallback.length > 0
  );
}

function safeVisibleAssetPathForRoute(routePathname: string): string | null {
  if (routePathname === "/") return "index";
  if (!routePathname.startsWith("/")) return null;

  const segments = routePathname.slice(1).split("/");
  if (
    segments.length === 0 ||
    segments.some((segment) => segment.length === 0 || segment === "." || segment === "..")
  ) {
    return null;
  }

  return segments.join("/");
}

/**
 * Publish an asset to its target. Foreign-collision detection happens earlier in
 * the plan phase, so every target reaching here is either absent or owned by a
 * prior run — in both cases the current source is written, overwriting stale
 * output so republished assets match the latest prerender. Copy through a
 * temp file + rename so a crash cannot leave a partially written asset. Returns
 * false only if the source disappears after planning.
 */
function publishAsset(sourcePath: string, targetPath: string): boolean {
  const tmpPath = `${targetPath}.vinext-tmp-${process.pid}`;
  try {
    fs.mkdirSync(path.dirname(targetPath), { recursive: true });
    fs.copyFileSync(sourcePath, tmpPath);
    fs.renameSync(tmpPath, targetPath);
    return true;
  } catch (error) {
    // Best-effort cleanup: when a parent path component is a file, even this
    // rmSync throws ENOTDIR, and it must not mask the fallback decision below.
    try {
      fs.rmSync(tmpPath, { force: true });
    } catch {
      // tmpPath cannot exist if its parent directory does not
    }
    // ENOENT: the planned source disappeared. The directory-shape conflicts
    // (a directory where the file target should be, or a file blocking a
    // parent directory — ENOTDIR on POSIX, EEXIST on Windows mkdir) mean this
    // route's visible HTML cannot exist on this filesystem layout; the Worker
    // keeps serving the route, so degrade to "not published" instead of
    // aborting the whole prerender. ENAMETOOLONG covers a deeply nested route
    // whose individual segments are short enough to pass the basename guard
    // above but whose full path exceeds the OS limit.
    const code = (error as NodeJS.ErrnoException).code;
    if (
      code === "ENOENT" ||
      code === "EISDIR" ||
      code === "ENOTDIR" ||
      code === "ENOTEMPTY" ||
      code === "EEXIST" ||
      code === "ENAMETOOLONG"
    ) {
      return false;
    }
    throw error;
  }
}

// Common filesystems cap a filename at 255 bytes, and publishAsset first
// writes a `.vinext-tmp-<pid>` sibling. Skip assets whose filename cannot fit
// instead of crashing the publish step with ENAMETOOLONG; the Worker serves
// the route at runtime because the static request falls through on asset miss.
const MAX_ASSET_FILENAME_BYTES = 255 - ".vinext-tmp-9999999".length;

function isPublishableAssetTarget(targetPath: string): boolean {
  return Buffer.byteLength(path.basename(targetPath), "utf-8") <= MAX_ASSET_FILENAME_BYTES;
}

function removeOwnedFileTarget(targetPath: string): void {
  try {
    const stat = fs.lstatSync(targetPath);
    if (!stat.isFile() && !stat.isSymbolicLink()) return;
    fs.rmSync(targetPath, { force: true });
  } catch (error) {
    if ((error as NodeJS.ErrnoException).code !== "ENOENT") throw error;
  }
}

function pruneOwnedStaticRscAssets(clientDir: string, desiredTargets: ReadonlySet<string>): void {
  const rscDir = path.join(clientDir, `.${VINEXT_STATIC_RSC_TRANSPORT_PREFIX}`);
  if (!fs.existsSync(rscDir)) return;

  const visit = (dir: string): boolean => {
    let remainingEntries = 0;
    for (const entry of fs.readdirSync(dir, { withFileTypes: true })) {
      const entryPath = path.join(dir, entry.name);
      if (entry.isDirectory()) {
        if (!visit(entryPath)) remainingEntries++;
        continue;
      }

      if (desiredTargets.has(entryPath)) {
        remainingEntries++;
        continue;
      }
      removeOwnedFileTarget(entryPath);
      if (fs.existsSync(entryPath)) remainingEntries++;
    }

    if (remainingEntries === 0) {
      fs.rmdirSync(dir);
      return true;
    }
    return false;
  };

  visit(rscDir);
}

function pruneOwnedAssets(
  clientDir: string,
  owned: ReturnType<typeof readGeneratedAssetOwnership>,
  desired: {
    html: ReadonlySet<string>;
    rsc: ReadonlySet<string>;
  },
): void {
  for (const pathname of owned.html) {
    if (desired.html.has(pathname)) continue;
    const visibleAssetPath = safeVisibleAssetPathForRoute(pathname);
    if (!visibleAssetPath) continue;
    const htmlAssetPath = pathname === "/" ? "index.html" : visibleAssetPath;
    removeOwnedFileTarget(path.join(clientDir, htmlAssetPath));
    // Remove directories the pruned file leaves empty: an ancestor route's
    // HTML target is a plain file at the directory's path, so a leftover empty
    // directory would keep that route unpublishable forever.
    let parentDir = path.dirname(path.join(clientDir, htmlAssetPath));
    while (parentDir !== clientDir && parentDir.startsWith(clientDir)) {
      try {
        fs.rmdirSync(parentDir);
      } catch {
        break;
      }
      parentDir = path.dirname(parentDir);
    }
  }

  if (owned.rsc) {
    pruneOwnedStaticRscAssets(clientDir, desired.rsc);
  }
}

function headerBlockForPath(options: {
  detachHeaders?: readonly string[];
  headers: Record<string, string>;
  pathname: string;
}): string[] {
  const { detachHeaders = [], headers, pathname } = options;
  const lines = [pathname];
  for (const name of detachHeaders) {
    lines.push(`  ! ${name}`);
  }
  for (const [name, value] of Object.entries(headers)) {
    lines.push(`  ${name}: ${value}`);
  }
  return lines;
}

function removeGeneratedHeadersBlock(content: string): string {
  const start = content.indexOf(GENERATED_HEADERS_START);
  if (start === -1) return content.replace(/\s*$/, "");

  const end = content.indexOf(GENERATED_HEADERS_END, start);
  if (end === -1) return content.slice(0, start).replace(/\s*$/, "");

  return (content.slice(0, start) + content.slice(end + GENERATED_HEADERS_END.length)).replace(
    /\s*$/,
    "",
  );
}

/**
 * Read the set of assets a previous run published, from the generated `_headers`
 * block. This is vinext's ownership record: a target that is recorded here is
 * ours to re-publish idempotently, whereas an existing target that is absent
 * here belongs to the user/build and must not be touched. HTML rules are listed
 * per route; static RSC is a single wildcard, and its transport namespace is
 * reserved, so the wildcard's presence proves ownership of every RSC target.
 */
function readGeneratedAssetOwnership(existingHeaders: string): {
  html: Set<string>;
  rsc: boolean;
} {
  const html = new Set<string>();
  let rsc = false;

  const start = existingHeaders.indexOf(GENERATED_HEADERS_START);
  if (start === -1) return { html, rsc };
  const end = existingHeaders.indexOf(GENERATED_HEADERS_END, start);
  const block = existingHeaders.slice(start, end === -1 ? undefined : end);

  const rscWildcard = `${VINEXT_STATIC_RSC_TRANSPORT_PREFIX}/*`;
  for (const line of block.split("\n")) {
    if (line.length === 0 || /^\s/.test(line) || line.startsWith("#")) continue;
    const pathname = line.trim();
    if (pathname === rscWildcard) rsc = true;
    else html.add(pathname);
  }
  return { html, rsc };
}

type HeaderEntry = {
  detachHeaders?: readonly string[];
  headers: Record<string, string>;
  pathname: string;
};

function writeGeneratedHeadersBlock(clientDir: string, entries: HeaderEntry[]): void {
  const headersPath = path.join(clientDir, "_headers");
  const existing = fs.existsSync(headersPath) ? fs.readFileSync(headersPath, "utf-8") : "";
  const preserved = removeGeneratedHeadersBlock(existing);
  if (entries.length === 0) {
    if (preserved.length > 0) {
      fs.writeFileSync(headersPath, `${preserved}\n`);
    } else {
      fs.rmSync(headersPath, { force: true });
    }
    return;
  }

  const generated = [
    GENERATED_HEADERS_START,
    ...entries.flatMap((entry) => headerBlockForPath(entry)),
    GENERATED_HEADERS_END,
    "",
  ].join("\n");

  const nextContent = preserved.length > 0 ? `${preserved}\n\n${generated}` : generated;
  fs.mkdirSync(clientDir, { recursive: true });
  fs.writeFileSync(headersPath, nextContent);
}

function staticCacheHeaders(contentType: string): Record<string, string> {
  return {
    "Content-Type": contentType,
    "Cache-Control": STATIC_CACHE_CONTROL,
    [VINEXT_CACHE_HEADER]: "STATIC",
    [NEXTJS_CACHE_HEADER]: "HIT",
  };
}

function buildHtmlHeaders(route: Extract<PrerenderRouteResult, { status: "rendered" }>) {
  return {
    ...staticCacheHeaders("text/html; charset=utf-8"),
    ...(route.headers?.link ? { Link: route.headers.link } : {}),
  };
}

function buildRscHeaders(options: {
  deploymentId: string | undefined;
  rscCompatibilityId: string | undefined;
}): Record<string, string> {
  return {
    ...staticCacheHeaders(VINEXT_RSC_CONTENT_TYPE),
    Vary: VINEXT_RSC_VARY_HEADER,
    ...(options.rscCompatibilityId
      ? { [VINEXT_RSC_COMPATIBILITY_ID_HEADER]: options.rscCompatibilityId }
      : {}),
    ...(options.deploymentId ? { [NEXTJS_DEPLOYMENT_ID_HEADER]: options.deploymentId } : {}),
  };
}

function eligibleStaticAppRoutes(
  routes: readonly PrerenderRouteResult[],
): Array<Extract<PrerenderRouteResult, { status: "rendered" }>> {
  return routes.filter((route): route is Extract<PrerenderRouteResult, { status: "rendered" }> => {
    const routePathname = route.status === "rendered" ? (route.path ?? route.route) : route.route;
    return (
      route.status === "rendered" &&
      route.router === "app" &&
      route.revalidate === false &&
      route.queryInvariant?.html === true &&
      route.fallback !== true &&
      routePathname !== "/404" &&
      routePathname !== "/500"
    );
  });
}

type PlannedAssetBase = {
  routePathname: string;
  source: string;
  target: string;
};

type PlannedAsset =
  | (PlannedAssetBase & {
      kind: "html";
      assetPath: string;
      route: Extract<PrerenderRouteResult, { status: "rendered" }>;
    })
  | (PlannedAssetBase & { kind: "rsc" });

type PublishCloudflarePrerenderedAppAssetsOptions = {
  config: ResolvedNextConfig;
  /**
   * Whether the built app contains any server action references. Server
   * actions POST to the visible page path (both the fetch client and the
   * progressive no-JS form fallback), and Cloudflare's asset worker claims a
   * published asset path for every method — non-GET/HEAD requests get a 405
   * from the asset worker, never reaching the user Worker. Visible HTML must
   * therefore not be published when actions exist; the reserved static RSC
   * transport path stays safe because nothing POSTs to it.
   */
  hasServerActions: boolean;
  prerenderDir: string;
  root: string;
  routes: readonly PrerenderRouteResult[];
  rscCompatibilityId?: string;
  serverDir: string;
};

type PublicationRouteFact = {
  htmlAssetPath: string;
  htmlSource: string;
  htmlSourceExists: boolean;
  htmlTarget: string;
  htmlTargetExists: boolean;
  htmlTargetIsDirectory: boolean;
  route: Extract<PrerenderRouteResult, { status: "rendered" }>;
  routePathname: string;
  rscSource: string;
  rscSourceExists: boolean;
  rscTarget: string;
  rscTargetExists: boolean;
};

type CloudflarePublicationFacts = {
  clientDir: string | null;
  config: ResolvedNextConfig;
  emittedRscTransportAllowed: boolean;
  existingHeaders: string;
  hasMiddlewareOrProxy: boolean;
  hasServerActions: boolean;
  owned: ReturnType<typeof readGeneratedAssetOwnership>;
  rootRscTransportAllowed: boolean;
  routeFacts: PublicationRouteFact[];
  rscCompatibilityId: string | undefined;
};

type CloudflarePublicationPlan =
  | {
      action: "skip";
      clientDir: string | null;
      owned: ReturnType<typeof readGeneratedAssetOwnership>;
      pruneOwned: boolean;
      reason: string;
    }
  | {
      action: "publish";
      assets: PlannedAsset[];
      clientDir: string;
      deploymentId: string | undefined;
      desired: {
        html: Set<string>;
        rsc: Set<string>;
      };
      owned: ReturnType<typeof readGeneratedAssetOwnership>;
      rscCompatibilityId: string | undefined;
    };

function collectCloudflarePublicationFacts(
  options: PublishCloudflarePrerenderedAppAssetsOptions,
): CloudflarePublicationFacts {
  const assetsConfig = readEmittedWranglerAssetsConfig(options.serverDir);
  const emittedAssets = assetsConfig.ok ? assetsConfig.assets : null;
  const clientDir = emittedAssets?.directory
    ? path.resolve(options.serverDir, emittedAssets.directory)
    : null;
  const headersPath = clientDir ? path.join(clientDir, "_headers") : null;
  const existingHeaders =
    headersPath && fs.existsSync(headersPath) ? fs.readFileSync(headersPath, "utf-8") : "";
  const owned = readGeneratedAssetOwnership(existingHeaders);
  const rootAssetsConfig = readRootWranglerAssetsConfig(options.root, process.env.CLOUDFLARE_ENV);
  const rootRscTransportAllowed =
    rootAssetsConfig.ok && isCloudflareRscTransportAllowedForAssetsConfig(rootAssetsConfig.assets);
  const routeFacts: PublicationRouteFact[] = [];

  if (clientDir) {
    for (const route of eligibleStaticAppRoutes(options.routes)) {
      const routePathname = route.path ?? route.route;
      const visibleAssetPath = safeVisibleAssetPathForRoute(routePathname);
      if (!visibleAssetPath) continue;

      let rscAssetPathname: string | null = null;
      try {
        rscAssetPathname = `${VINEXT_STATIC_RSC_TRANSPORT_PREFIX}${createRscTransportAssetPathname(
          routePathname,
        )}`;
      } catch {
        rscAssetPathname = null;
      }
      if (!rscAssetPathname) continue;

      const htmlAssetPath = routePathname === "/" ? "index.html" : visibleAssetPath;
      const htmlSource = path.join(options.prerenderDir, getOutputPath(routePathname, false));
      const htmlTarget = path.join(clientDir, htmlAssetPath);
      const htmlTargetExists = fs.existsSync(htmlTarget);
      const rscSource = path.join(options.prerenderDir, getRscOutputPath(routePathname));
      const rscTarget = path.join(clientDir, `.${rscAssetPathname}`);

      routeFacts.push({
        htmlAssetPath,
        htmlSource,
        htmlSourceExists: fs.existsSync(htmlSource),
        htmlTarget,
        htmlTargetExists,
        htmlTargetIsDirectory: htmlTargetExists ? fs.statSync(htmlTarget).isDirectory() : false,
        route,
        routePathname,
        rscSource,
        rscSourceExists: fs.existsSync(rscSource),
        rscTarget,
        rscTargetExists: fs.existsSync(rscTarget),
      });
    }
  }

  return {
    clientDir,
    config: options.config,
    emittedRscTransportAllowed: emittedAssets
      ? isCloudflareRscTransportAllowedForAssetsConfig(emittedAssets)
      : false,
    existingHeaders,
    hasMiddlewareOrProxy: hasMiddlewareOrProxy(options.root, options.config),
    hasServerActions: options.hasServerActions,
    owned,
    rootRscTransportAllowed,
    routeFacts,
    rscCompatibilityId: options.rscCompatibilityId,
  };
}

function skipPublication(
  facts: CloudflarePublicationFacts,
  reason: string,
  pruneOwned: boolean,
): CloudflarePublicationPlan {
  return { action: "skip", clientDir: facts.clientDir, owned: facts.owned, reason, pruneOwned };
}

function planCloudflarePublication(facts: CloudflarePublicationFacts): CloudflarePublicationPlan {
  const skip = (reason: string, pruneOwned: boolean) => skipPublication(facts, reason, pruneOwned);

  if (facts.config.output === "export") {
    return skip("static export already writes to client assets", true);
  }
  if (facts.config.trailingSlash) {
    return skip("trailingSlash would require preserving slash redirects", true);
  }
  if (facts.config.basePath) {
    return skip("basePath asset route publication is not proven safe", true);
  }
  if (facts.config.i18n) {
    return skip("i18n routing may require Worker redirects or locale negotiation", true);
  }
  if (hasRequestTransformConfig(facts.config)) {
    return skip("config headers, redirects, or rewrites require Worker routing", true);
  }
  if (facts.hasMiddlewareOrProxy) {
    return skip("middleware/proxy must run before page responses", true);
  }
  if (!facts.rootRscTransportAllowed) {
    return skip("Cloudflare RSC transport is disabled for the selected Wrangler environment", true);
  }
  if (!facts.clientDir) {
    return skip("Cloudflare assets binding not found", false);
  }
  if (!facts.emittedRscTransportAllowed) {
    return skip("Cloudflare assets not_found_handling is not none", true);
  }

  const plannedAssets: PlannedAsset[] = [];

  for (const routeFact of facts.routeFacts) {
    // An existing target is a foreign collision only when a prior vinext run did
    // not publish it; targets we own are re-published so their headers survive.
    // A directory at the HTML target is not a foreign file: it is the parent of
    // descendant route assets (owned ones are pruned before publication) and
    // publishAsset degrades safely if it persists.
    const htmlForeign =
      routeFact.htmlTargetExists &&
      !routeFact.htmlTargetIsDirectory &&
      !facts.owned.html.has(routeFact.routePathname);
    const rscForeign = routeFact.rscTargetExists && !facts.owned.rsc;
    if (htmlForeign || rscForeign) {
      continue;
    }

    if (
      !facts.hasServerActions &&
      routeFact.htmlSourceExists &&
      isPublishableAssetTarget(routeFact.htmlTarget)
    ) {
      plannedAssets.push({
        kind: "html",
        assetPath: routeFact.htmlAssetPath,
        route: routeFact.route,
        routePathname: routeFact.routePathname,
        source: routeFact.htmlSource,
        target: routeFact.htmlTarget,
      });
    }

    if (routeFact.route.queryInvariant?.rsc === true) {
      if (routeFact.rscSourceExists && isPublishableAssetTarget(routeFact.rscTarget)) {
        plannedAssets.push({
          kind: "rsc",
          routePathname: routeFact.routePathname,
          source: routeFact.rscSource,
          target: routeFact.rscTarget,
        });
      }
    }
  }

  // A route's visible HTML target is a plain file, so it cannot coexist with a
  // descendant route whose target needs that same path as a directory (e.g.
  // `/blog` -> `dist/client/blog` vs `/blog/post` -> `dist/client/blog/post`).
  // Publish the descendants and let the Worker keep serving the ancestor
  // document; skipping HTML never changes behavior, only the fast path. The
  // tokenised RSC transport is flat and cannot conflict.
  const plannedHtmlAssetPaths: string[] = [];
  for (const asset of plannedAssets) {
    if (asset.kind === "html") plannedHtmlAssetPaths.push(asset.assetPath);
  }
  const assets = plannedAssets.filter((asset) => {
    if (asset.kind !== "html") return true;
    const dirPrefix = `${asset.assetPath}/`;
    return !plannedHtmlAssetPaths.some((assetPath) => assetPath.startsWith(dirPrefix));
  });

  let generatedRuleCount = 0;
  const desiredHtmlRoutes = new Set<string>();
  const desiredRscTargets = new Set<string>();
  for (const asset of assets) {
    if (asset.kind === "html") {
      generatedRuleCount++;
      desiredHtmlRoutes.add(asset.routePathname);
    } else {
      desiredRscTargets.add(asset.target);
    }
  }
  if (desiredRscTargets.size > 0) {
    generatedRuleCount++;
  }

  const preservedRuleCount = countHeaderRules(removeGeneratedHeadersBlock(facts.existingHeaders));
  if (preservedRuleCount + generatedRuleCount > CLOUDFLARE_HEADERS_RULE_LIMIT) {
    return skip(
      `publishing static assets would exceed Cloudflare's ${CLOUDFLARE_HEADERS_RULE_LIMIT}-rule _headers limit`,
      true,
    );
  }

  return {
    action: "publish",
    assets,
    clientDir: facts.clientDir,
    deploymentId: facts.config.deploymentId,
    desired: {
      html: desiredHtmlRoutes,
      rsc: desiredRscTargets,
    },
    owned: facts.owned,
    rscCompatibilityId: facts.rscCompatibilityId,
  };
}

function applyCloudflarePublicationPlan(
  plan: CloudflarePublicationPlan,
): PublishCloudflarePrerenderedAppAssetsResult {
  if (plan.action === "skip") {
    if (plan.pruneOwned && plan.clientDir) {
      pruneOwnedAssets(plan.clientDir, plan.owned, { html: new Set(), rsc: new Set() });
      writeGeneratedHeadersBlock(plan.clientDir, []);
    }
    return skipResult(plan.reason);
  }

  pruneOwnedAssets(plan.clientDir, plan.owned, plan.desired);

  const headerEntries: HeaderEntry[] = [];
  let publishedStaticRsc = false;
  let publishedFiles = 0;
  const publishedRoutePathnames = new Set<string>();

  // Re-copy owned targets so repeated prerenders refresh stale bodies and keep
  // the generated header block authoritative.
  for (const asset of plan.assets) {
    if (!publishAsset(asset.source, asset.target)) continue;
    publishedFiles++;
    publishedRoutePathnames.add(asset.routePathname);
    if (asset.kind === "rsc") {
      publishedStaticRsc = true;
      continue;
    }
    headerEntries.push({
      pathname: asset.routePathname,
      detachHeaders: HTML_AUTHORITATIVE_HEADERS,
      headers: buildHtmlHeaders(asset.route),
    });
  }

  if (publishedStaticRsc) {
    headerEntries.push({
      pathname: `${VINEXT_STATIC_RSC_TRANSPORT_PREFIX}/*`,
      detachHeaders: RSC_AUTHORITATIVE_HEADERS,
      headers: buildRscHeaders({
        deploymentId: plan.deploymentId,
        rscCompatibilityId: plan.rscCompatibilityId,
      }),
    });
  }

  writeGeneratedHeadersBlock(plan.clientDir, headerEntries);
  return { skipped: false, publishedFiles, publishedRoutes: publishedRoutePathnames.size };
}

