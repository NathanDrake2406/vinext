#!/usr/bin/env node
/**
 * Paired Workers benchmark for the static App Router asset split transport
 * (cloudflare/vinext#2506).
 *
 * head = tests/fixtures/cf-app-basic (middleware.ts removed) built with the
 *        local vinext CLI, serving published static HTML + transport RSC.
 * base = byte-identical copy of the head build with the published route
 *        assets removed, so the same requests fall through to the Worker.
 *
 * Both wrangler dev servers stay alive for the whole run; samples alternate
 * AB/BA per round so drift affects both variants symmetrically.
 *
 * Usage: node benchmarks/perf/static-artifact-worker-bypass.mjs
 *   [--rounds=8] [--warmup=250] [--requests=1600] [--concurrency=8]
 */

import { execFileSync, spawn } from "node:child_process";
import fs from "node:fs";
import os from "node:os";
import path from "node:path";
import { fileURLToPath, pathToFileURL } from "node:url";

const repoRoot = path.dirname(path.dirname(path.dirname(fileURLToPath(import.meta.url))));
const fixtureDir = path.join(repoRoot, "tests/fixtures/cf-app-basic");
const cliPath = path.join(repoRoot, "packages/vinext/dist/cli.js");

function intArg(name, fallback) {
  const raw = process.argv.find((argument) => argument.startsWith(`--${name}=`));
  if (!raw) return fallback;
  const value = Number(raw.slice(name.length + 3));
  if (!Number.isInteger(value) || value < 1) {
    throw new Error(`--${name} must be a positive integer, received ${raw}`);
  }
  return value;
}

const rounds = intArg("rounds", 8);
const warmupRequests = intArg("warmup", 250);
const measuredRequests = intArg("requests", 1600);
const concurrency = intArg("concurrency", 8);

// Markers written by packages/vinext/src/build/cloudflare-prerender-assets.ts.
// The block doubles as the ownership manifest of published route assets, so
// the base variant can strip exactly what the publisher wrote.
const GENERATED_HEADERS_START = "# Static prerendered App Router assets (generated by vinext)";
const GENERATED_HEADERS_END = "# End static prerendered App Router assets";

const transport = await import(
  pathToFileURL(path.join(repoRoot, "packages/vinext/dist/server/app-rsc-transport.js")).href
);

function staticRscUrl(routePathname) {
  return `${transport.VINEXT_STATIC_RSC_TRANSPORT_PREFIX}${transport.createRscTransportAssetPathname(routePathname)}?_rsc`;
}

const scenarios = [
  { name: "document-about", pathname: "/about", accept: "text/html" },
  {
    name: "rsc-about-static-transport",
    pathname: staticRscUrl("/about"),
    accept: "text/x-component",
  },
  { name: "document-blog", pathname: "/blog/hello-world", accept: "text/html" },
  {
    name: "rsc-blog-static-transport",
    pathname: staticRscUrl("/blog/hello-world"),
    accept: "text/x-component",
  },
  { name: "route-api-control", pathname: "/api/hello", accept: "*/*" },
];

function copyFixture(targetDir) {
  fs.mkdirSync(targetDir, { recursive: true });
  for (const entry of fs.readdirSync(fixtureDir)) {
    // middleware forces every request through the Worker and disables static
    // publication, which is exactly what this benchmark measures against.
    if (entry === "middleware.ts" || entry === "node_modules" || entry === "dist") continue;
    fs.cpSync(path.join(fixtureDir, entry), path.join(targetDir, entry), { recursive: true });
  }
  // The fixture's node_modules is a pnpm workspace install; link back to it so
  // the copied app resolves the locally built vinext.
  fs.symlinkSync(path.join(fixtureDir, "node_modules"), path.join(targetDir, "node_modules"));
}

function stripPublishedAssets(buildDir) {
  const clientDir = path.join(buildDir, "dist/client");
  const headersPath = path.join(clientDir, "_headers");
  const headersContent = fs.readFileSync(headersPath, "utf-8");
  const start = headersContent.indexOf(GENERATED_HEADERS_START);
  const end = headersContent.indexOf(GENERATED_HEADERS_END);
  if (start === -1 || end === -1) {
    throw new Error("head build did not publish static route assets; nothing to compare");
  }
  const block = headersContent.slice(start, end);

  let removed = 0;
  for (const line of block.split(/\r?\n/)) {
    // Top-level (non-indented) lines inside the block are header rule
    // pathnames — one per published HTML route plus the RSC wildcard.
    if (!line.startsWith("/") || line.includes("*")) continue;
    const pathname = line.trim();
    const htmlFile = pathname === "/" ? "index.html" : `.${pathname}`;
    fs.rmSync(path.join(clientDir, htmlFile), { force: true });
    removed++;
  }
  const rscDir = path.join(clientDir, `.${transport.VINEXT_STATIC_RSC_TRANSPORT_PREFIX}`);
  if (fs.existsSync(rscDir)) {
    fs.rmSync(rscDir, { recursive: true });
    removed++;
  }
  fs.writeFileSync(
    headersPath,
    headersContent.slice(0, start) + headersContent.slice(end + GENERATED_HEADERS_END.length),
  );
  if (removed === 0) throw new Error("no published assets found to strip from the base build");
}

function startServer(appDir, port) {
  const child = spawn(
    path.join(fixtureDir, "node_modules/.bin/wrangler"),
    [
      "dev",
      "--config",
      "dist/server/wrangler.json",
      "--port",
      String(port),
      // Each server needs its own inspector port or the second one aborts on
      // the default 9229.
      "--inspector-port",
      String(port + 100),
      "--show-interactive-dev-session=false",
    ],
    { cwd: appDir, stdio: ["ignore", "pipe", "pipe"] },
  );
  let output = "";
  child.stdout.on("data", (chunk) => (output += chunk));
  child.stderr.on("data", (chunk) => (output += chunk));
  child.getOutput = () => output;
  return child;
}

async function waitReady(port, child) {
  const deadline = Date.now() + 120_000;
  while (Date.now() < deadline) {
    if (child.exitCode !== null) {
      throw new Error(`wrangler dev exited early:\n${child.getOutput()}`);
    }
    try {
      const response = await fetch(`http://127.0.0.1:${port}/api/hello`);
      await response.arrayBuffer();
      if (response.ok) return;
    } catch {
      // not listening yet
    }
    await new Promise((resolve) => setTimeout(resolve, 250));
  }
  throw new Error(`wrangler dev on port ${port} not ready in time:\n${child.getOutput()}`);
}

async function runSample(port, scenario, requestCount) {
  const durations = [];
  let nextRequest = 0;
  async function worker() {
    while (nextRequest < requestCount) {
      nextRequest++;
      const startedAt = performance.now();
      const response = await fetch(`http://127.0.0.1:${port}${scenario.pathname}`, {
        headers: { accept: scenario.accept },
      });
      await response.arrayBuffer();
      if (!response.ok) {
        throw new Error(`${scenario.name} returned ${response.status} on port ${port}`);
      }
      durations.push(performance.now() - startedAt);
    }
  }
  await Promise.all(Array.from({ length: concurrency }, worker));
  return durations;
}

function mean(values) {
  return values.reduce((total, value) => total + value, 0) / values.length;
}

async function main() {
  if (!fs.existsSync(cliPath)) {
    throw new Error(`vinext CLI not built at ${cliPath}; run \`vp run vinext#build\` first`);
  }

  const headDir = fs.mkdtempSync(path.join(os.tmpdir(), "vinext-bypass-head-"));
  const baseDir = fs.mkdtempSync(path.join(os.tmpdir(), "vinext-bypass-base-"));
  console.log(`head: ${headDir}`);
  console.log(`base: ${baseDir}`);

  copyFixture(headDir);
  console.log("building head (vinext build --prerender-all)...");
  execFileSync(process.execPath, [cliPath, "build", "--prerender-all"], {
    cwd: headDir,
    stdio: "inherit",
  });

  copyFixture(baseDir);
  fs.cpSync(path.join(headDir, "dist"), path.join(baseDir, "dist"), { recursive: true });
  stripPublishedAssets(baseDir);

  const headPort = 8931;
  const basePort = 8932;
  // Reclaim the dev and inspector ports from any servers a previous aborted
  // run left behind; a stale listener otherwise fails or, worse, gets measured.
  for (const port of [headPort, basePort, headPort + 100, basePort + 100]) {
    try {
      const pids = execFileSync("lsof", ["-ti", `tcp:${port}`], { encoding: "utf8" })
        .split("\n")
        .filter(Boolean);
      for (const pid of pids) process.kill(Number(pid), "SIGKILL");
    } catch {
      // lsof exits non-zero when the port is free
    }
  }
  const servers = [startServer(headDir, headPort), startServer(baseDir, basePort)];
  const stopServers = () => {
    for (const server of servers) server.kill("SIGTERM");
  };
  process.on("SIGINT", () => {
    stopServers();
    process.exit(130);
  });

  try {
    await Promise.all([waitReady(headPort, servers[0]), waitReady(basePort, servers[1])]);

    // Sanity gate: the head variant must actually serve /about from assets and
    // the base variant from the Worker, or the comparison measures nothing.
    const headProbe = await fetch(`http://127.0.0.1:${headPort}/about`);
    await headProbe.arrayBuffer();
    const baseProbe = await fetch(`http://127.0.0.1:${basePort}/about`);
    await baseProbe.arrayBuffer();
    if (headProbe.headers.get("x-vinext-cache") !== "STATIC") {
      throw new Error("head /about is not served as a published static asset");
    }
    if (baseProbe.headers.get("x-vinext-cache") === "STATIC") {
      throw new Error("base /about still serves the published static asset");
    }

    const samples = new Map(
      scenarios.flatMap((scenario) => [
        [`head:${scenario.name}`, []],
        [`base:${scenario.name}`, []],
      ]),
    );

    for (let round = 0; round < rounds; round++) {
      const order =
        round % 2 === 0
          ? [
              ["head", headPort],
              ["base", basePort],
            ]
          : [
              ["base", basePort],
              ["head", headPort],
            ];
      for (const [variant, port] of order) {
        for (const scenario of scenarios) {
          await runSample(port, scenario, warmupRequests);
          const durations = await runSample(port, scenario, measuredRequests);
          samples.get(`${variant}:${scenario.name}`).push(...durations);
          process.stdout.write(
            `round ${round + 1}/${rounds} ${variant} ${scenario.name}: ${mean(durations).toFixed(4)}ms\n`,
          );
        }
      }
    }

    const rows = scenarios.map((scenario) => {
      const baseMean = mean(samples.get(`base:${scenario.name}`));
      const headMean = mean(samples.get(`head:${scenario.name}`));
      const diff = headMean - baseMean;
      return {
        scenario: scenario.name,
        baseMean,
        headMean,
        diff,
        diffPercent: (diff / baseMean) * 100,
      };
    });

    const timestamp = new Date()
      .toISOString()
      .replaceAll(":", "-")
      .replace(/\.\d+Z$/, "Z");
    const resultDir = path.join(
      repoRoot,
      "benchmarks/results/static-artifact-worker-bypass",
      timestamp,
    );
    fs.mkdirSync(resultDir, { recursive: true });
    const summaryLines = [
      "# Static App Router Asset Split Transport Benchmark",
      "",
      `- head: ${headDir}`,
      `- base: ${baseDir}`,
      `- rounds: ${rounds}`,
      `- warmup/request shape: ${warmupRequests}, excluded`,
      `- measured/request shape: ${measuredRequests}`,
      `- concurrency: ${concurrency}`,
      "",
      "| scenario | base mean ms | head mean ms | diff ms | diff % |",
      "| --- | ---: | ---: | ---: | ---: |",
      ...rows.map(
        (row) =>
          `| ${row.scenario} | ${row.baseMean.toFixed(4)} | ${row.headMean.toFixed(4)} | ${row.diff.toFixed(4)} | ${row.diffPercent.toFixed(2)}% |`,
      ),
    ];
    fs.writeFileSync(path.join(resultDir, "summary.md"), `${summaryLines.join("\n")}\n`);
    fs.writeFileSync(
      path.join(resultDir, "results.json"),
      JSON.stringify(
        { headDir, baseDir, rounds, warmupRequests, measuredRequests, concurrency, rows },
        null,
        2,
      ),
    );
    console.log(`\n${summaryLines.join("\n")}`);
    console.log(`\nresults written to ${resultDir}`);
  } finally {
    stopServers();
  }
}

await main();
